// INCLUDES.
#include "Input.h"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

// CONSTRUCTORS & DESTRUCTOR.
Input::Input()
{
	// set default values
	mouse.left = false;
	mouse.right = false;
	mouse.x = 0;
	mouse.y = 0;
}

Input::~Input()
{
	//std::cout << "Input object destroyed!\n";
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

// FUCNTIONS.

void Input::setButtonDown(int button)
{
	if (button >= 0)
	{
		buttons[button] = true;
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setButtonUp(int button)
{
	if (button >= 0)
	{
		buttons[button] = false;
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Input::isButtonDown(int button)
{
	if (button >= 0)
	{
		return buttons[button];
	}

	return false;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setMouseX(int lx)
{
	mouse.x = lx;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setMouseY(int ly)
{
	mouse.y = ly;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setMousePosition(int lx, int ly)
{
	setMouseX(lx);
	setMouseY(ly);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

int Input::getMouseX()
{
	return mouse.x;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

int Input::getMouseY()
{
	return mouse.y;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setMouseLDown(bool down)
{
	mouse.left = down;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Input::isMouseLDown()
{
	return mouse.left;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setMouseRDown(bool down)
{
	mouse.right = down;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Input::isMouseRDown()
{
	return mouse.right;
}

